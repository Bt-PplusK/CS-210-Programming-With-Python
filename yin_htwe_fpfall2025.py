def check_balance(current_balance):
    print(f"Your Current Balance: ${current_balance:.2f} ")

def deposit_money(current_balance):
    try:
        amount = float(input("Enter amount to deposit: $"))
        if amount > 0:
            current_balance += amount
            print(f"Successfully deposited ${amount:.2f}.")
        else:
            print("Error: Deposit amount must be positive.")
    except ValueError:
        print("Error: Invalid input. Please enter a number.")
    return current_balance

def withdraw_money(current_balance):
    try:
        amount = float(input("Enter amount to withdraw: $"))
        if amount <= 0:
            print("Error: Withdrawal amount must be positive.")
        elif amount > current_balance:
            print("Error: Insufficient funds.") # Handling insufficient funds
        else:
            current_balance -= amount
            print(f"${amount:.2f} withdrawn successfully.")
    except ValueError:
        print("Error: Invalid input. Please enter a number.")
    return current_balance

