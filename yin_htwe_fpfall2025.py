"""Number Guessing Game with Score Tracker

Project Description: A user guesses a randomly generated number between 1 and 100.

Features:

• Use a function to run the guessing game logic.

• Use a loop to allow multiple attempts and games.

• Use decision structures to provide feedback: "Too high", "Too low", or "Correct!".

• Track the number of attempts and use Boolean expressions to end the game on a correct guess
or user quit. (100 marks)
"""

import random

def play_one_round():
    target_number = random.randint(1, 100)
    attempts = 0
    game_active = True # Boolean flag for session control

    print("Guess the number between 1 and 100. Type 'q' to quit.")

    while game_active:
        user_input = input("Enter your guess: ")

        #Check if user wants to quit
        if user_input.lower() == 'q':
            print("You quit the game.")
            return False # Return False because they didn't win

        # Validate input is a number
        if not user_input.isdigit():
            print("Invalid input. Please enter a number or 'q'.")
            continue # Skip to the start of the loop

        # Game Logic
        guess = int(user_input)
        attempts += 1

        if guess == target_number:
            print(f"Correct! You guessed the number in {attempts} attempts.")
            return True # Return True because they won
        elif guess > target_number:
            print("Too high! Try again.")
        else:
            print("Too low! Try again.")

def main():
    print("=== Welcome to the Number Guessing Game ===")
    total_games_played = 0
    games_won = 0
    running = True # Boolean flag for main application loop

    while running:
        choice = input("Do you want to play a game? (y/n): ").lower()

        if choice == 'y':
            total_games_played += 1
            has_won = play_one_round()

            if has_won:
                games_won += 1

        elif choice == 'n':
            print("Thanks for playing!")
            print(f"Total games played: {total_games_played}")
            print(f"Games won: {games_won}")
            running = False # End the loop

        else:
            print("Invalid choice. Please enter 'y' or 'n'.")

if __name__ == "__main__":
    main()


